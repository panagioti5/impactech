public class Foo {
    private double num;
    private String name;

    public Foo(double num, String name) {
        this.num = num;
        this.name = name;
    }

    public double getNum() {
        return num;
    }

    public void setNum(double num) {
        this.num = num;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
